package Day03;

public class RelationalOperators {

    public static void main(String[] args) {

        System.out.println("Relational Operators  < :"+(45 < 89));
        System.out.println("Relational Operators  > :"+(45 > 89));
        System.out.println("Relational Operators  <= :"+(45 <= 89));
        System.out.println("Relational Operators  >= :"+(45 >= 89));
        System.out.println("Relational Operators  == :"+(45 == 89));
        System.out.println("Relational Operators  != :"+(45 != 89));




    }
}
