package Day03;

public class ArithmeticOperators {
    public static void main(String[] args) {
        System.out.println("Arithmetic Operators + "+(45+90));
        System.out.println("Arithmetic Operators - "+(45-90));
        System.out.println("Arithmetic Operators * "+(45*90));
        System.out.println("Arithmetic Operators / "+(4567/9));
        System.out.println("Arithmetic Operators % "+(4567%9));
    }
}
