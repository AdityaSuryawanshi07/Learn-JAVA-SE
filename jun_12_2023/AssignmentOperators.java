package jun_12_2023;

public class AssignmentOperators {

	public static void main(String[] args) {
		
		int a = 90;
		
		System.out.println(a);
		
//		a = a+10;
		a +=10;
		System.out.println(a);
		a *=10;
		System.out.println(a);
		a /=10;
		System.out.println(a);
		a -=10;
		System.out.println(a);
		a %=10;
		System.out.println(a);

	}

}
