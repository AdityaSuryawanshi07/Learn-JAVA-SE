package jun_12_2023;

public class RelationalOperators {

	public static void main(String[] args) {
		
		System.out.println("Relational Operators < "+(89 < 56));
		System.out.println("Relational Operators > "+(89 > 56));
		System.out.println("Relational Operators <= "+(89 <= 56));
		System.out.println("Relational Operators >= "+(89 >= 56));
		System.out.println("Relational Operators == "+(89 == 56));
		
		System.out.println("Relational Operators != "+(89 != 56));

	}

}
